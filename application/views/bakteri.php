	<div class="body">
		<p>Bakteri adalah organisme mikroskopis yang tidak terlihat dengan mata telanjang. Mereka dapat hidup di
			lingkungan yang beragam, baik di dalam maupun di luar tubuh manusia (seperti tanah, air sungai, serta air
			laut).
			Bakteri dianggap sebagai organisme purba yang pertama menghuni bumi, yakni sekitar empat miliar tahun yang
			lalu.
			Secara struktur, bakteri dikategorikan sebagai prokariota yang merupakan organisme bersel tunggal. Sel
			bakteri terdiri dari:
		</p>
		<img src="https://2.bp.blogspot.com/-uslnG-b5omg/XE_ETz7HA-I/AAAAAAAACrs/ZqVRBk-NJMIptv3qkth0jwJZiL6x3xgQwCLcBGAs/s1600/Struktur-Sel-Bakteri-Dan-Fungsinya.jpg">
		<ul>
			<li><b>Kapsul</b> </li>
			<p>Lapisan yang terdapat di luar dinding sel.</p>
			<li><b>Dinding sel</b></li>
			<p>Bagian ini terletak di luar membran plasma. Salah satu fungsinya adalah memberi bentuk pada bakteri. </p>
			<li><b>Membran plasma</b></li>
			<p>Membran plasma dapat ditemukan dalam dinding sel. Membran ini bertugas mengangkut bahan-bahan kimia.</p>
			<li><b>Sitoplasma</b></li>
			<p>Zat gelatin yang terdapat di membran plasma ini mengandung bahan genetik dan ribosom.</p>
			<li><b>DNA</b></li>
			<p>DNA terletak di dalam sitoplasma dan berperan dalam perkembangan serta fungsi bakteri.</p>
			<li><b>Ribosom</b></li>
			<p>Partikel kompleks ini menjadi tempat protein dibuat atau disintesis.</p>
			<li><b>Flagelum</b></li>
			<p>Ini adalah alat yang digunakan oleh bakteri untuk bergerak.</p>
			<li><b>Pili</b></li>
			<p>Struktur ini berkontribusi terhadap penyebaran penyakit, karena memungkinkan sel bakteri memindahkan
				bahan genetik ke sel-sel lain.</p>
		</ul>
		<h1><b>1. Kenali jenis bakteri dari bentuknya</b></h1>
		<p>Pengertian bakteri takkan lengkap tanpa mengetahui jenis-jenis bakteri. Terdapat tiga bentuk bakteri yang
			paling umum. Berikut penjelasannya:
		</p>
		<ul>
			<li><b>Bulat</b></li>
			<p>Bakteri yang berbentuk bulat disebut cocci untuk penyebutan jamak atau coccus untuk penyebutan
				tunggalnya. Contohnya adalah <i> S. pneumoniae</i>.</p>
			<li><b>Silinder</b></li>
			<p>Bakteri yang berbentuk silinder seperti kapsul dikenal sebagai <i> bacilli </i> untuk penyebutan
				jamak dan <i> bacillus</i>untuk penyebutan tunggalnya. Contoh bakteri silinder adalah <i> Lactobacillus
					acidophilus</i>.</p>
			<li><b>Spiral</b></li>
			<p>Bakteri yang berbentuk spiral dikenal dengan nama <i> spirillia </i>untuk penyebutan jamak dan<i>
					spirillium </i>untuk penyebutan tunggalnya. Contohnya adalah <i> Spirochete</i>.</p>
		</ul>
		<h1><b>2. Jenis bakteri berdasarkan warnanya</b></h1>
		<p> Sejak tahun 1800an, Hans Christian Gram mengembangkan pewarnaan gram pada bakteri. Ilmuwan ini menggunakan
			empat komponen warna, yaitu violet kristal, mordan (larutan iodin), pencuci zat warna yang berupa alkohol
			atau aseton, dan safranin.
			Pewarnaan tersebut bermanfaat untuk memudahkan penggolongan bakteri. Ketika diwarnai, akan tampak warna biru
			pada bakteri gram positif dan merah pada bakteri gram negatif.
			Sebagai contoh, saat bakteri memiliki warna, tampak bulat di bawah mikroskop, dan berwarna biru, bakteri ini
			sudah bisa dipastikan sebagai <i> Staphylococcus</i>.</p>

		<h1><b>3. Penyakit akibat bakteri yang sering menyerang orang Indonesia</b></h1>
		<ul>
			<li><b> Infeksi E.coli</b></li>
			<p>Meski umumnya tidak berbahaya, ada beberapa jenis bakteri <i> Escherichia coli</i> (E.coli) yang bisa
				memicu
				gangguan
				pencernaan. Gejala infeksi bakteri ini umumnya meliputi diare, muntah-muntah, kram perut, hingga darah
				pada
				tinja.</p>
			<li><b>Difteri</b></li>
			<p>Penyakit ini disebabkan oleh bakteri <i> Corynebacterium diphtheria</i>. Ciri khas difteri adalah
				terbentuknya
				selaput berwarna abu-abu yang melapisi tenggorokan dan amandel, sehingga penderita mengalami kesulitan
				bernapas.

				Sementara gejala difteri pada tahap awal bisa berupa demam, sakit tenggorokan, serta pembengkakan pada
				kelenjar getah bening di leher.</p>
			<li><b>Infeksi saluran pernapasan bawah</b></li>
			<p>Infeksi ini menyerang paru-paru atau bagian bawah kotak suara. Penyakit-penyakit yang termasuk di
				dalamnya
				adalah
				pneumonia, bronkitis, dan tuberkulosis.

				Selain virus dan jamur, infeksi saluran pernapasan bawah juga dapat disebabkan oleh bakteri. Conothnya,
				<i> Streptococcus </i>atau <i> Staphylococcus aureus</i>.</p>
		</ul>
